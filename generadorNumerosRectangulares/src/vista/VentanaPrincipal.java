    private void jbuttonEncontrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbuttonEncontrarActionPerformed
        // TODO add your handling code here:
        //limpiarTabla();
         // a jframe here isn't strictly necessary, but it makes the example a little more real
        JFrame frame = new JFrame("buscar generador");
        String ent = JOptionPane.showInputDialog(frame, "ingrese el valor para m");
        try{
            int m=Integer.parseInt(ent);
            int valorMin=2;
            if(m>valorMin){
                gcMixto=new GCMixto(m);
                if(gcMixto.esGeneradorCompleto()){
                    jTextFieldA.setText(gcMixto.getA()+"");
                    jTextFieldC.setText(gcMixto.getC()+"");
                    jTextFieldM.setText(gcMixto.getM()+"");
                    jTextFieldXo.setText(gcMixto.getXo()+"");
                    jLabelMensaje.setText("");
                }else{
                    JOptionPane.showMessageDialog(frame, "no existe generador completo para "+m);
                }
            }else{
                JOptionPane.showMessageDialog(null, "el valor de m debe ser mayor a "+valorMin);
            }
        }catch(Exception e){
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(frame, "el valor de m debe ser un numero");
        }
        
    }//GEN-LAST:event_jbuttonEncontrarActionPerformed

    private void jButtonGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGenerarActionPerformed
        // TODO add your handling code here:
        limpiarTabla();
        try{
            int a=Integer.parseInt(jTextFieldA.getText());
            int c=Integer.parseInt(jTextFieldC.getText());
            int m=Integer.parseInt(jTextFieldM.getText());
            int xo=Integer.parseInt(jTextFieldXo.getText());
            if(verificarDatos(a,c,m,xo)){
                gcMixto=new GCMixto(a, c, m, xo);
                gcMixto.generarNumeros2();
                if(jRmixto.isSelected()){
                    if(gcMixto.esPeriodoCompleto()){
                        jLabelMensaje.setText("generador de periodo completo "+m);
                        jLabelMensaje.setForeground(Color.blue);
                    }else{    
                        jLabelMensaje.setText("NO es un generador de periodo completo ");
                        jLabelMensaje.setForeground(Color.red);
                    }
                }else{
                    
                }
                agregarFilas(gcMixto.getResultadoGenerado());
                
            }else{
                JOptionPane.showMessageDialog(null, "los numeros no son validos");
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "introduzca numeros plox");
        }
    }//GEN-LAST:event_jButtonGenerarActionPerformed
    private boolean verificarDatos(int a,int c,int m,int xo){
        return m>a&&m>c&&m>xo;
    }
    private void agregarFilas(ArrayList<GCMixto> listaRes){
        Object[] aux;
        int cont=0;
        if(jRmixto.isSelected()){
            for(GCMixto gc:listaRes){
                aux=new Object[6];
                aux[0]=cont;
                aux[1]=gc.getXo();
                aux[2]=gc.getA()*gc.getXo();
                aux[3]=gc.getA()*gc.getXo()+gc.getC();
                aux[4]=gc.getXn();
                aux[5]=gc.getUn();
                model.addRow(aux);
                cont++;
            }
        }else{
            for(GCMixto gc:listaRes){
                aux=new Object[6];
                aux[0]=cont;
                aux[1]=gc.getXo();
                aux[2]=gc.getA()*gc.getXo();
                aux[3]=gc.getXn();
                aux[4]=gc.getUn();
                model.addRow(aux);
                cont++;
            }
        }
        
    }
